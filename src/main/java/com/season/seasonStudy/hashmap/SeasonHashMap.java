    public V get(Object key) {
        Node<K,V> e;
        //对查找元素的key进行hash映射到HashMap的哈希桶上，得到数组下标，然后将数组下标和key都传入getNode()方法
        return (e = getNode(hash(key), key)) == null ? null : e.value;
    }

    final Node<K,V> getNode(int hash, Object key) {
        Node<K,V>[] tab; Node<K,V> first, e; int n; K k;
        //tab代表哈希表，n代表哈希表的长度，first = tab[(n-1) & hash]是取hash值对应数组下标的首个元素
        if ((tab = table) != null && (n = tab.length) > 0 &&
                (first = tab[(n - 1) & hash]) != null) {
            //判断首个元素是否是待查找的元素
            if (first.hash == hash && // always check first node
                    ((k = first.key) == key || (key != null && key.equals(k))))
                return first;
            //判断是否有下一个元素
            if ((e = first.next) != null) {
                //判断第一个元素是不是一个树结点
                if (first instanceof TreeNode)
                    //从红黑树中找目标元素
                    return ((TreeNode<K,V>)first).getTreeNode(hash, key);
                //头结点不是一个树结点，则是一条链表，遍历链表查找目标元素
                do {
                    //目标元素的hash和key都等于传入的参数值，返回此结点
                    if (e.hash == hash &&
                            ((k = e.key) == key || (key != null && key.equals(k))))
                        return e;
                } while ((e = e.next) != null);
            }
        }
        //找不到目标元素
        return null;
    }


    final Node<K,V>[] resize() {
        Node<K,V>[] oldTab = table;
        int oldCap = (oldTab == null) ? 0 : oldTab.length;
        int oldThr = threshold;
        int newCap, newThr = 0;
        //如果旧容量等于0则代表是第一次初始化
        if (oldCap > 0) {
            //太狠了，如果大于最大容量，直接将上限阈值调成int最大值，而且不再进行扩容
            if (oldCap >= MAXIMUM_CAPACITY) {
                threshold = Integer.MAX_VALUE;
                return oldTab;
            }
            //如果扩容两倍后依然小于最大容量且旧容量大于默认容量16
            else if ((newCap = oldCap << 1) < MAXIMUM_CAPACITY &&
                    oldCap >= DEFAULT_INITIAL_CAPACITY)
                //扩容2倍
                newThr = oldThr << 1; // double threshold
        }
        //扩容后的容量就是初始最小阈值16
        else if (oldThr > 0) // initial capacity was placed in threshold
            newCap = oldThr;
            //如果哈希表没有初始化且最小容量阈值指定为0，则新的容量就是默认容量16，新的扩容阈值就是装载因子*默认容量
        else {               // zero initial threshold signifies using defaults
            newCap = DEFAULT_INITIAL_CAPACITY;
            newThr = (int)(DEFAULT_LOAD_FACTOR * DEFAULT_INITIAL_CAPACITY);
        }
        //如果新的扩容阈值为0
        if (newThr == 0) {
            float ft = (float)newCap * loadFactor;
            newThr = (newCap < MAXIMUM_CAPACITY && ft < (float)MAXIMUM_CAPACITY ?
                    (int)ft : Integer.MAX_VALUE);
        }
        threshold = newThr;
        @SuppressWarnings({"rawtypes","unchecked"})
        Node<K,V>[] newTab = (Node<K,V>[])new Node[newCap];
        //扩容操作，将新的哈希表赋值给table
        table = newTab;
        //如果原来的哈希表不为空
        if (oldTab != null) {
            //遍历哈希表中的元素
            for (int j = 0; j < oldCap; ++j) {
                Node<K,V> e;
                //如果下标为j的哈希桶上存在元素，赋值给e
                if ((e = oldTab[j]) != null) {
                    //去除哈希桶上的元素
                    oldTab[j] = null;
                    //如果只有一个元素则直接搬到新的桶中去
                    if (e.next == null)
                        newTab[e.hash & (newCap - 1)] = e;
                        //如果是一棵红黑树
                    else if (e instanceof TreeNode)
                        //红黑树大法搬移数据
                        ((TreeNode<K,V>)e).split(this, newTab, j, oldCap);
                    else { // preserve order
                        //链表复制，jdk1.8优化
                        Node<K,V> loHead = null, loTail = null;
                        Node<K,V> hiHead = null, hiTail = null;
                        Node<K,V> next;
                        do {
                            next = e.next;
                            if ((e.hash & oldCap) == 0) { // index =0
                                if (loTail == null)
                                    loHead = e;
                                else
                                    loTail.next = e;
                                loTail = e;
                            }
                            else {
                                if (hiTail == null)
                                    hiHead = e;
                                else
                                    hiTail.next = e;
                                hiTail = e;
                            }
                        } while ((e = next) != null);
                        if (loTail != null) {
                            loTail.next = null;
                            newTab[j] = loHead;
                        }
                        if (hiTail != null) {
                            hiTail.next = null;
                            newTab[j + oldCap] = hiHead;
                        }
                    }
                }
            }
        }
        return newTab;
    }




    public V put(K key, V value) {
        //计算出哈希桶的位置
        return putVal(hash(key), key, value, false, true);
    }

    final V putVal(int hash, K key, V value, boolean onlyIfAbsent,
            boolean evict) {
        Node<K,V>[] tab; Node<K,V> p; int n, i;
        //判断哈希表是否还没初始化
        if ((tab = table) == null || (n = tab.length) == 0)
            //哈希表扩容，并把新的哈希表长度赋值n，把扩容的哈希表返回给tab
            n = (tab = resize()).length;
        //判断哈希桶中是否空的
        if ((p = tab[i = (n - 1) & hash]) == null)
            //空的，放在桶的第一个位置上
            tab[i] = newNode(hash, key, value, null);
        else {
            Node<K,V> e; K k;
            //如果key已经存在且hash值相等则直接覆盖value
            if (p.hash == hash &&
                    ((k = p.key) == key || (key != null && key.equals(k))))
                e = p;
                //如果key不存在，则判断插入的结点是否是红黑树结点
            else if (p instanceof TreeNode)
                //往红黑树中插入结点
                e = ((TreeNode<K,V>)p).putTreeVal(this, tab, hash, key, value);
            else {
                //遍历链表，binCount记录链表长度
                for (int binCount = 0; ; ++binCount) {
                    //遍历到最后一个元素
                    if ((e = p.next) == null) {
                        //插入到尾部，JDK1.8的尾插法
                        p.next = newNode(hash, key, value, null);
                        //如果链表长度大于树化的阈值，则尝试转化为红黑树
                        if (binCount >= TREEIFY_THRESHOLD - 1) // -1 for 1st
                            treeifyBin(tab, hash);
                        break;
                    }
                    //如果存在相同的key且hash值相同，则覆盖value值
                    if (e.hash == hash &&
                            ((k = e.key) == key || (key != null && key.equals(k))))
                        break;
                    p = e;
                }
            }
            //如果e存在则证明存在相同key，覆盖value值
            if (e != null) { // existing mapping for key
                V oldValue = e.value;
                if (!onlyIfAbsent || oldValue == null)
                    e.value = value;
                afterNodeAccess(e);
                return oldValue;
            }
        }
        ++modCount;
        //超过哈希表的最大容量则扩容
        if (++size > threshold)
            resize();
        afterNodeInsertion(evict);
        return null;
    }






















    /**
     * 获取哈希值，然后高位与低位异或
     */
    static final int hash(Object key) {
        int h;
        return (key == null) ? 0 : (h = key.hashCode()) ^ (h >>> 16);
    }

    /**
     * 哈希桶
     */
    static class Node<K, V> implements Map.Entry<K, V> {

        final int hash;
        final K key;
        V value;
        Node<K, V> next;

        Node(int hash, K key, V value, Node<K, V> next) {
            this.hash = hash;
            this.key = key;
            this.value = value;
            this.next = next;
        }

        public final int hashcode() {
            return Objects.hashCode(key) ^ Objects.hashCode(value);
        }

        public final boolean equals(Object o) {
            if (this == o) {
                return true;
            } else if (o instanceof Map.Entry) {
                // Map.Entry<K, V> entry = (Map.Entry<K, V>)o; // 数据类型不一样怎么办，数据类型一个是Object，一个是Integer
                Map.Entry<?, ?> entry = (Map.Entry<?, ?>)o;
                return Objects.equals(key, entry.getKey()) && Objects.equals(value, entry.getValue());
            }
            return false;
        }

        @Override
        public String toString() {
            return key + "=" + value;
        }

        @Override
        public K getKey() {
            return key;
        }

        @Override
        public V getValue() {
            return value;
        }

        @Override
        public V setValue(V value) {
            V oldValue = this.value;
            this.value = value;
            return oldValue;
        }
    }

    /**
     * The default initial capacity - MUST be a power of two.
     */
    static final int DEFAULT_INITIAL_CAPACITY = 1 << 4; // aka 16

    /**
     * The maximum capacity, used if a higher value is implicitly specified
     * by either of the constructors with arguments.
     * MUST be a power of two <= 1<<30.
     */
    static final int MAXIMUM_CAPACITY = 1 << 30;

    /**
     * The load factor used when none specified in constructor.
     */
    static final float DEFAULT_LOAD_FACTOR = 0.75f;

    /**
     * The bin count threshold for using a tree rather than list for a
     * bin.  Bins are converted to trees when adding an element to a
     * bin with at least this many nodes. The value must be greater
     * than 2 and should be at least 8 to mesh with assumptions in
     * tree removal about conversion back to plain bins upon
     * shrinkage.
     */
    static final int TREEIFY_THRESHOLD = 8;

    /**
     * The bin count threshold for untreeifying a (split) bin during a
     * resize operation. Should be less than TREEIFY_THRESHOLD, and at
     * most 6 to mesh with shrinkage detection under removal.
     */
    static final int UNTREEIFY_THRESHOLD = 6;

    /**
     * The smallest table capacity for which bins may be treeified.
     * (Otherwise the table is resized if too many nodes in a bin.)
     * Should be at least 4 * TREEIFY_THRESHOLD to avoid conflicts
     * between resizing and treeification thresholds.
     */
    static final int MIN_TREEIFY_CAPACITY = 64;

    int threshold;

    /**
     * The load factor for the hash table.
     *
     * @serial
     */
    float loadFactor;

























